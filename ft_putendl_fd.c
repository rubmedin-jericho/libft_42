#include "libft.h"

void	ft_putendl_fd(char	*s, int	fd)
{
	int	i;
	
	i = 0;
	while(s[i])	
	{
		if(s[i] == '\\' && s[i + 1] == 'n')
		{
			write(fd, "\n", 1);
			i += 2;
		}
		else
		{
			write(fd, &s[i], 1);
			i++;
		}
	}

}
